class Exercise_008{
    public static void main(String[] args) {
        double celcius, fahrenheit;
        // Assign a value to celcius
        celcius = 34.0;
        fahrenheit = (celcius * 9/5) + 32;
        // Print the result
        System.out.println(celcius + " degrees Celcius is equal to " + fahrenheit + " degrees Fahrenheit.");
    }
}