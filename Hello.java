// This is a simple Java program that prints "Hello World, I love Java!" to the console.
class Hello{
    // The main method is the entry point of any Java program.
    public static void main(String[] args){
        // Print the message to the console.
        // The System.out.println() method is used to print text to the console.
        // The text inside the quotes is the message that will be printed.
        System.out.println("Hello World, I love Java!");
    }
}