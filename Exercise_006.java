class Exercise_006{
    // Write a program that calculates the area of a wall. The wall is 23 feet long and 12 feet high.
    public static void main(String[] args) {
        // Declare variables for length and width
        int length, width;
        // Assign values to length and width
        length = 23;
        width = 12;
        // Calculate the area of the wall
        int area = length * width;
        // Print the result
        System.out.println("The area of the wall is " + area + " square feet.");
    }
}